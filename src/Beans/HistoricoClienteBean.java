package Beans;

public class HistoricoClienteBean {
	
	
	private String Descricao_Material;
	private int Quantidade_Fardos;
	private double Peso_Kg;
	private String Data_Emissao;
	private String Situacao_Pedido;
	
	public String getDescricao_Material() {
		return Descricao_Material;
	}
	public void setDescricao_Material(String descricao_Material) {
		Descricao_Material = descricao_Material;
	}
	public int getQuantidade_Fardos() {
		return Quantidade_Fardos;
	}
	public void setQuantidade_Fardos(int quantidade_Fardos) {
		Quantidade_Fardos = quantidade_Fardos;
	}
	public double getPeso_Kg() {
		return Peso_Kg;
	}
	public void setPeso_Kg(double peso_Kg) {
		Peso_Kg = peso_Kg;
	}
	public String getData_Emissao() {
		return Data_Emissao;
	}
	public void setData_Emissao(String data_Emissao) {
		Data_Emissao = data_Emissao;
	}
	public String getSituacao_Pedido() {
		return Situacao_Pedido;
	}
	public void setSituacao_Pedido(String situacao_Pedido) {
		Situacao_Pedido = situacao_Pedido;
	}
	
	
	

}
